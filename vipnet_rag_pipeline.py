# ----- 9.1 Токенизация для BM25 -----
def tokenize(text: str):
    """Токенизация: нижний регистр, только буквы и цифры."""
    text = text.lower()
    text = re.sub(r"[^\w\d]+", " ", text)
    return text.split()

