def is_cli_cmd(text: str, mono: bool) -> bool:
    """Проверяет, является ли строка началом CLI-команды."""
    t = text.strip().lower()
    return (mono or any(t.startswith(v) for v in CLI_VERBS)) and len(text) < 180

